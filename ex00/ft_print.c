#include "rush01.h"

void	ft_print(t_box **box, int size)
{
	int		i;
	int		j;
	char	tmp;

	i = 0;
	j = 0;
	while (j < (size + 2))
	{
		i = 0;
		while (i < size + 2)
		{
			tmp = (box[i][j].value) + 48;
			write(1, &tmp, 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}

